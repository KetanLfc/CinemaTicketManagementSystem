    private void updateMovieTable() {
        DefaultTableModel model = (DefaultTableModel) movieTable.getModel();
        model.setRowCount(0);

        for (Movie movie : movies) {
            model.addRow(new Object[]{movie.getId(), movie.getName(), movie.getDuration(), movie.getLanguage(), movie.getGenre()});
        }
    }
    
    private void addMovieButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addMovieButtonActionPerformed
        
        String id = movieIdField.getText();
        String name = movieNameField.getText();
        String duration = durationField.getText();
        String language = languageField.getText();
        String genre = genreField.getText();

        if (id.isEmpty() || name.isEmpty() || duration.isEmpty() || language.isEmpty() || genre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all fields.");
            return;
        }

        Movie movie = new Movie(id, name, duration, language, genre);
        movies.add(movie);
        updateMovieTable();
        updateSelectMovieComboBox();
        clearMovieFields();
    }//GEN-LAST:event_addMovieButtonActionPerformed

    private void searchMovieBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchMovieBtnActionPerformed
         
        String id = movieIdField.getText();
        String name = movieNameField.getText();
        String duration = durationField.getText();
        String language = languageField.getText();
        String genre = genreField.getText();

        DefaultTableModel model = (DefaultTableModel) movieTable.getModel();
        model.setRowCount(0);

        for (Movie movie : movies) {
            if (movie.getId().equals(id) || movie.getName().equals(name) || movie.getDuration().equals(duration) || movie.getLanguage().equals(language) || movie.getGenre().equals(genre)) {
                model.addRow(new Object[]{movie.getId(), movie.getName(), movie.getDuration(), movie.getLanguage(), movie.getGenre()});
            }
        }

        if (model.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "No movie found.");
        }
    }//GEN-LAST:event_searchMovieBtnActionPerformed

    private void deleteMovieBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteMovieBtnActionPerformed
        
        int selectedRow = movieTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a movie to delete.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) movieTable.getModel();
        String movieId = model.getValueAt(selectedRow, 0).toString();

        movies.removeIf(movie -> movie.getId().equals(movieId));
        updateMovieTable();
        updateSelectMovieComboBox();
    }//GEN-LAST:event_deleteMovieBtnActionPerformed

    private void updateSelectMovieComboBox() {
        selectMovieComboBox.removeAllItems();
        for (Movie movie : movies) {
            selectMovieComboBox.addItem(movie.getName());
        }
    }
    

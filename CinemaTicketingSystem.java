    private void deleteTicketBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteTicketBtnActionPerformed
        
   int selectedRow = ticketTable.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a ticket to delete.");
        return;
    }

    DefaultTableModel model = (DefaultTableModel) ticketTable.getModel();
    String customerName = model.getValueAt(selectedRow, 0).toString();
    String movieShowTitle = model.getValueAt(selectedRow, 1).toString();
    String theaterName = model.getValueAt(selectedRow, 2).toString();
    String hallNumber = model.getValueAt(selectedRow, 3).toString();
    String seatNumber = model.getValueAt(selectedRow, 4).toString();

    tickets.removeIf(ticket -> ticket.getCustomerName().equals(customerName) &&
                               ticket.getMovieShowTitle().equals(movieShowTitle) &&
                               ticket.getTheaterName().equals(theaterName) &&
                               ticket.getHallNumber().equals(hallNumber) &&
                               ticket.getSeatNumber().equals(seatNumber));
    updateTicketTable();
    }//GEN-LAST:event_deleteTicketBtnActionPerformed


    private void addCustomerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCustomerBtnActionPerformed
         
        String id = customerIdField.getText();
        String firstName = firstNameField.getText();
        String lastName = lastNameField.getText();

        if (id.isEmpty() || firstName.isEmpty() || lastName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all fields.");
            return;
        }

        Customer customer = new Customer(id, firstName, lastName);
        customers.add(customer);
        updateCustomerTable();
        updateCustomerComboBox();
        clearCustomerFields();
    }

    private void clearCustomerFields() {
        customerIdField.setText("");
        firstNameField.setText("");
        lastNameField.setText("");
    }

    private void updateCustomerTable() {
        DefaultTableModel model = (DefaultTableModel) customerTable.getModel();
        model.setRowCount(0);

        for (Customer customer : customers) {
            model.addRow(new Object[]{customer.getId(), customer.getFirstName(), customer.getLastName()});
        }
    
    }//GEN-LAST:event_addCustomerBtnActionPerformed

    private void searchCustomerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchCustomerBtnActionPerformed
        
        String id = customerIdField.getText();
        String firstName = firstNameField.getText();
        String lastName = lastNameField.getText();

        DefaultTableModel model = (DefaultTableModel) customerTable.getModel();
        model.setRowCount(0);

        for (Customer customer : customers) {
            if (customer.getId().equals(id) || customer.getFirstName().equals(firstName) || customer.getLastName().equals(lastName)) {
                model.addRow(new Object[]{customer.getId(), customer.getFirstName(), customer.getLastName()});
            }
        }

        if (model.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "No customer found.");
        }
    }//GEN-LAST:event_searchCustomerBtnActionPerformed

    private void deleteCustomerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteCustomerButtonActionPerformed
         
        int selectedRow = customerTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a customer to delete.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) customerTable.getModel();
        String customerId = model.getValueAt(selectedRow, 0).toString();

        customers.removeIf(customer -> customer.getId().equals(customerId));
        updateCustomerTable();
        updateCustomerComboBox();
    }//GEN-LAST:event_deleteCustomerButtonActionPerformed

